# Function for extracting the individual links of each movie from the main list. It receives a soup object -- an Object generated by BeautifulSoup -- 
# and extracts the link of each individual movie's page.
def extract_movies(soup):
    movies_table = soup.find_all('a', href=True)
    titles = re.findall(r'/title/\w+', str(movies_table)) # Regex command to find all strings with characters '/title/' in them, 
    #the w+ command shows the following characters of the referred string
    movie_links = ['https://imdb.com' + title for title in titles] # Creating complete link by concatenating the string 'https://imdb.com' with the '/title/...' generated by the instruction above
    for i in range(len(movie_links)//2):
        if movie_links[i] == movie_links[i+1]:
            movie_links.remove(movie_links[i+1]) # Removing repeated strings/links

    threads = min(MAX_THREADS, len(movie_links)) # Generating threads to be used on the processing of the insructions
    with concurrent.futures.ThreadPoolExecutor(max_workers=threads) as executor:
        executor.map(extract_movie_details, movie_links)


    


def main():
    start_time = time.time() # Starting timer

    # IMDB Most Popular Movies - 100 movies
    popular_movies_url = 'https://www.imdb.com/chart/moviemeter/?ref_=nv_mv_mpm'
    response = requests.get(popular_movies_url, headers=headers)
    soup = BeautifulSoup(response.content, 'html.parser')

    # Main function to extract the 100 movies from IMDB Most Popular Movies
    extract_movies(soup)

    end_time = time.time() # Finishing timer
    print("Total time taken: ", end_time - start_time)


if __name__ == '__main__':
    main()
