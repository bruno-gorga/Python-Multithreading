# Global header for use in web request operation
headers = {'User-Agent': 'Mozilla/5.0 (Macintosh; Intel Mac OS X 10_10_1) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/39.0.2171.95 Safari/537.36'}

MAX_THREADS = 10 # Number of threads to be used

# Function for extracting the movie data. It receives a link extracted from a soup object -- the main page of IMDB's Top 100 Movies -- 
# and scraps the info from them using html tags with BeautifulSoup. Then it creatres a .csv file with extracted data.
def extract_movie_details(movie_link):
    response = BeautifulSoup(requests.get(movie_link, headers=headers).content, 'html.parser')
    movie_soup = response

    if movie_soup is not None:
        title = None 
